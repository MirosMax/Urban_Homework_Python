example = 'Это я удачно зашел!'
print(example[0])  # первый символ
print(example[-1])  # последний символ строки (используя отрицательный индекс)
print(example[int(len(example) / 2):])  # вторую половину строки
print(example[::-1])  # строка наоборот
print(example[1::2])  # каждый второй символ строки